import React, { useState, useEffect, useRef } from 'react';
import { Dialog, DialogContent, DialogHeader, DialogTitle } from "@/components/ui/dialog";
import { Button } from "@/components/ui/button";
import { Input } from "@/components/ui/input";
import { supabase } from "@/integrations/supabase/client";
import { ChatMessage } from "@/types/supabase";
import { cn } from "@/lib/utils";
import { useToast } from "@/components/ui/use-toast";
import { X } from 'lucide-react';

interface AdminChatModalProps {
  isOpen: boolean;
  onClose: () => void;
  userEmail: string;
  chatId: string;
}

const AdminChatModal: React.FC<AdminChatModalProps> = ({ isOpen, onClose, userEmail, chatId }) => {
  const { toast } = useToast();
  const [messages, setMessages] = useState<ChatMessage[]>([]);
  const [newMessage, setNewMessage] = useState("");
  const [uploadedImages, setUploadedImages] = useState<File[]>([]);
  const messagesEndRef = useRef<HTMLDivElement>(null);
  const subscriptionRef = useRef<ReturnType<typeof supabase.channel> | null>(null);
  const messagesContainerRef = useRef<HTMLDivElement>(null);

  const scrollToBottom = () => {
    messagesEndRef.current?.scrollIntoView({ behavior: "smooth" });
  };

  useEffect(() => {
    if (isOpen && chatId) {
      fetchMessages();
    }
  }, [isOpen, chatId]);

  const fetchMessages = async () => {
    try {
      // R√©cup√©rer la session admin
      const { data: { session } } = await supabase.auth.getSession();
      if (!session?.user?.email) {
        console.error('‚ùå Pas de session admin trouv√©e');
        return;
      }

      console.log('üë®‚Äçüíº Session admin trouv√©e:', session.user.email);

      // Marquer ce chat comme ouvert par l'admin
      const { error: upsertError } = await supabase
        .from('admin_chat_opened')
        .upsert({
          user_email: userEmail,
          admin_email: session.user.email,
          opened_at: new Date().toISOString()
        })
        .select('*')
        .maybeSingle();

      if (upsertError) {
        console.error('‚ùå Erreur lors de l\'enregistrement de la session de chat:', upsertError);
      }

      // R√©cup√©rer les messages
      const { data, error } = await supabase
        .from('chatbot_messages')
        .select('*')
        .eq('chat_id', chatId)
        .order('timestamp', { ascending: true });

      if (error) {
        console.error('‚ùå Erreur lors de la r√©cup√©ration des messages:', error);
        return;
      }

      if (data) {
        setMessages(data);
        
        // Marquer les messages comme lus
        const unreadMessages = data.filter(msg => !msg.read && msg.sender === 'user');
        if (unreadMessages.length > 0) {
          await supabase
            .from('chatbot_messages')
            .update({ read: true })
            .eq('chat_id', chatId)
            .eq('sender', 'user')
            .eq('read', false);
        }
      }
    } catch (error) {
      console.error('‚ùå Erreur lors de la r√©cup√©ration des messages:', error);
    }
  };

  useEffect(() => {
    if (isOpen && chatId) {
      fetchMessages();

      // Souscrire aux changements en temps r√©el
      console.log('üîÑ Mise en place de la souscription temps r√©el pour chat_id:', chatId);
      const channel = supabase
        .channel(`chat:${chatId}`)
        .on(
          'postgres_changes',
          {
            event: '*',
            schema: 'public',
            table: 'chatbot_messages',
            filter: `chat_id=eq.${chatId}`
          },
          async (payload) => {
            console.log('üì© Nouveau changement re√ßu:', payload);
            const msg = payload.new as ChatMessage;
            
            if (payload.eventType === 'INSERT') {
              console.log('‚ú® Nouveau message √† ajouter');
              setMessages(prev => {
                // V√©rifier si le message existe d√©j√†
                if (prev.find(m => m.id === msg.id)) {
                  console.log('‚ö†Ô∏è Message d√©j√† pr√©sent, ignor√©');
                  return prev;
                }
                console.log('‚úÖ Ajout du nouveau message');
                return [...prev, msg];
              });
              
              // Si c'est un message utilisateur, le marquer comme lu
              if (msg.sender === 'user' && !msg.read) {
                await supabase
                  .from('chatbot_messages')
                  .update({ read: true })
                  .eq('id', msg.id);
              }
            } else if (payload.eventType === 'DELETE') {
              console.log('üóëÔ∏è Suppression de la conversation d√©tect√©e');
              onClose();
            }
          }
        )
        .subscribe(status => {
          console.log('üì° Statut de la souscription:', status);
        });

      subscriptionRef.current = channel;

      return () => {
        console.log('üßπ Nettoyage de la souscription temps r√©el');
        if (subscriptionRef.current) {
          subscriptionRef.current.unsubscribe();
        }
      };
    }
  }, [isOpen, chatId]);

  // Cleanup lors de la fermeture du chat
  useEffect(() => {
    return () => {
      const cleanup = async () => {
        if (subscriptionRef.current) {
          subscriptionRef.current.unsubscribe();
        }

        console.log('üßπ Nettoyage du chat pour:', userEmail);

        // Supprimer l'entr√©e de la table admin_chat_opened
        try {
          const { error } = await supabase
            .from('admin_chat_opened')
            .delete()
            .eq('user_email', userEmail)
            .select();

          if (error) {
            console.error('‚ùå Erreur lors de la suppression de la session de chat:', error);
          } else {
            console.log('‚úÖ Session de chat supprim√©e');
          }
        } catch (error) {
          console.error('‚ùå Erreur lors du nettoyage:', error);
        }
      };

      cleanup();
    };
  }, [userEmail]);

  useEffect(() => {
    // Scroll vers le bas √† chaque nouveau message
    scrollToBottom();
  }, [messages]);

  const handleSendMessage = async () => {
    if (!newMessage.trim() && uploadedImages.length === 0) return;

    try {
      // Envoyer le message texte √† Supabase d'abord
      if (newMessage.trim()) {
        console.log('üì§ Envoi du message admin:', {
          email: userEmail,
          chat_id: chatId,
          message: newMessage,
          timestamp: new Date().toISOString()
        });

        const message = {
          email: userEmail,
          chat_id: chatId,
          message: newMessage,
          timestamp: new Date().toISOString(),
          sender: 'admin',
          read: true
        };

        const { data, error } = await supabase
          .from('chatbot_messages')
          .insert([message])
          .select('*')
          .single();

        if (error) {
          console.error('‚ùå Erreur lors de l\'insertion du message:', error);
          throw error;
        }
        if (data) {
          console.log('üü¢ Message admin ins√©r√© avec succ√®s:', data);
          setMessages(prev => [...prev, data]);
          setNewMessage("");

          // ‚úÖ Appel vers la fonction Edge pour d√©clencher Omnisend
          console.group('üì§ Appel √† la fonction Edge notify_user_on_admin_reply');
          
          // Attendre 1 seconde pour que Supabase ait fini la mise √† jour de client_has_replied_since_last_admin
          await new Promise(res => setTimeout(res, 1000));
          
          try {
            console.log('üîç Pr√©paration de la requ√™te:', {
              email: userEmail,
              chat_id: chatId,
              messageLength: newMessage.length,
              timestamp: new Date().toISOString()
            });

            // üëá Ping activit√© client pour mettre √† jour updated_at (√©vite faux positifs)
            try {
              const now = new Date().toISOString();
              const { error: pingError } = await supabase
                .from('client_chat_opened')
                .update({ updated_at: now })
                .eq('chat_id', chatId);

              if (pingError) {
                console.warn("‚ö†Ô∏è Ping d'activit√© √©chou√© (mais on continue):", pingError);
              } else {
                console.log("üì° Ping d'activit√© effectu√© avec succ√®s pour", userEmail);
              }
            } catch (e) {
              console.warn("‚ö†Ô∏è Erreur inattendue pendant le ping (ignor√©e):", e);
            }

            const { data: responseData, error: invokeError } = await supabase.functions.invoke('notify_user_on_admin_reply', {
              body: {
                record: {
                  email: userEmail,
                  chat_id: chatId,
                  message: newMessage,
                  sender: "admin",
                  id: data.id // On passe l'ID du message qui vient d'√™tre cr√©√©
                }
              }
            });

            if (invokeError) {
              console.error("‚ùå Erreur lors de l'appel √† la fonction Edge:", invokeError);
              
              toast({
                title: "‚ùå √âchec de l'envoi d'email",
                description: "Impossible d'envoyer l'email de notification au client. Veuillez r√©essayer.",
                variant: "destructive",
                duration: 5000
              });
            } else {
              console.log("‚úÖ Fonction Edge appel√©e avec succ√®s:", responseData);
              
              toast({
                title: "‚úÖ Email envoy√©",
                description: "Le client a √©t√© notifi√© par email de votre r√©ponse.",
                duration: 3000
              });
            }
          } catch (err) {
            console.error("‚ùå Erreur d'appel √† la fonction Edge:", {
              error: err,
              message: err instanceof Error ? err.message : "Erreur inconnue"
            });
          } finally {
            console.groupEnd();
          }
        }
      }

      // G√©rer l'upload des images si pr√©sentes
      if (uploadedImages.length > 0) {
        console.log('üì§ Upload de', uploadedImages.length, 'image(s)');
        for (const file of uploadedImages) {
          const fileExt = file.name.split('.').pop();
          const fileName = `${Date.now()}-${Math.random().toString(36).substring(2)}.${fileExt}`;
          
          console.log('üì§ Upload de l\'image:', fileName);
          const { data: uploadData, error: uploadError } = await supabase.storage
            .from("public-images")
            .upload(`chat_images/${fileName}`, file);

          if (uploadError) {
            console.error('‚ùå Erreur lors de l\'upload de l\'image:', uploadError);
            throw uploadError;
          }

          const publicURL = supabase.storage
            .from("public-images")
            .getPublicUrl(`chat_images/${fileName}`).data.publicUrl;

          console.log('‚úÖ Image upload√©e avec succ√®s:', publicURL);

          // Sauvegarder dans Supabase
          console.log('üì§ Envoi du message image admin');
          const { data: messageData, error: messageError } = await supabase
            .from('chatbot_messages')
            .insert({
              email: userEmail,
              chat_id: chatId,
              message: publicURL,
              timestamp: new Date().toISOString(),
              sender: 'admin',
              read: true
            })
            .select('*')
            .single();

          if (messageError) {
            console.error('‚ùå Erreur lors de l\'insertion du message image:', messageError);
            throw messageError;
          }
          if (messageData) {
            console.log('üü¢ Message image admin ins√©r√© avec succ√®s:', messageData);
            setMessages(prev => [...prev, messageData]);
          }
        }
        setUploadedImages([]);
      }
    } catch (error) {
      console.error('‚ùå Erreur lors de l\'envoi du message:', error);
    }
  };

  // Am√©lioration du scroll
  useEffect(() => {
    const shouldScroll = messagesContainerRef.current && 
      messagesContainerRef.current.scrollHeight - messagesContainerRef.current.scrollTop === messagesContainerRef.current.clientHeight;
    
    if (shouldScroll) {
      scrollToBottom();
    }
  }, [messages]);

  const formatDate = (date: string) => {
    return new Date(date).toLocaleString();
  };

  if (!isOpen) return null;

  return (
    <div className="fixed inset-0 bg-black bg-opacity-50 flex items-center justify-center z-50">
      <div className="bg-white rounded-lg w-full max-w-2xl h-[600px] flex flex-col">
        <div className="p-4 border-b flex justify-between items-center">
          <h2 className="text-xl font-semibold">Chat avec {userEmail}</h2>
          <button onClick={onClose} className="text-gray-500 hover:text-gray-700">
            <X size={24} />
          </button>
        </div>
        <div className="flex-1 overflow-y-auto p-4 space-y-4 bg-gray-50/50">
          {messages.map((message) => (
            <div
              key={message.id}
              className={cn(
                "flex items-start gap-2",
                message.sender === 'admin' && "flex-row-reverse"
              )}
            >
              <div className="w-8 h-8 rounded-full bg-primary flex items-center justify-center flex-shrink-0">
                <span className="text-white text-sm">
                  {message.sender === 'admin' ? 'üë®‚Äçüíº' : 'üë§'}
                </span>
              </div>
              
              <div className={cn(
                "rounded-2xl p-3 max-w-[80%] shadow-sm space-y-1",
                message.sender === 'admin' 
                  ? "bg-primary text-white rounded-tr-none" 
                  : "bg-white rounded-tl-none"
              )}>
                {message.message.match(/\.(jpeg|jpg|png|gif|webp)$/i) && message.message.startsWith("http") ? (
                  <img
                    src={message.message}
                    alt="Image envoy√©e"
                    className="max-w-xs rounded-lg shadow"
                  />
                ) : (
                  <p className="text-sm">{message.message}</p>
                )}
                <p className={cn(
                  "text-xs",
                  message.sender === 'admin' 
                    ? "text-white/80" 
                    : "text-gray-500"
                )}>
                  {formatDate(message.timestamp)}
                </p>
              </div>
            </div>
          ))}
          <div ref={messagesEndRef} />
        </div>
        <div className="p-4 border-t">
          <div className="flex gap-2">
            <Input
              placeholder="√âcrivez votre message..."
              value={newMessage}
              onChange={(e) => setNewMessage(e.target.value)}
              onKeyDown={(e) => {
                if (e.key === 'Enter' && !e.shiftKey) {
                  e.preventDefault();
                  handleSendMessage();
                }
              }}
            />
            <Button 
              onClick={handleSendMessage}
              disabled={!newMessage.trim() && uploadedImages.length === 0}
            >
              Envoyer
            </Button>
          </div>
          <div className="flex items-center gap-2">
            <label
              htmlFor="admin-image-upload"
              className={cn(
                "flex items-center gap-2 px-4 py-2 rounded-xl transition-colors text-sm font-medium border cursor-pointer",
                "bg-white text-primary border-primary/20 hover:bg-primary/5"
              )}
            >
              <span>üìé</span>
              <span>Ajouter des images</span>
            </label>
            <input
              id="admin-image-upload"
              type="file"
              accept="image/*"
              multiple
              onChange={(e) => {
                const files = Array.from(e.target.files || []);
                if (files.length + uploadedImages.length > 3) {
                  alert("Tu peux envoyer jusqu'√† 3 images max !");
                  return;
                }
                setUploadedImages((prev) => [...prev, ...files]);
              }}
              className="hidden"
            />
            {uploadedImages.length > 0 && (
              <div className="flex items-center gap-2">
                <span className="text-sm text-gray-500">
                  {uploadedImages.length} image{uploadedImages.length > 1 ? 's' : ''} s√©lectionn√©e{uploadedImages.length > 1 ? 's' : ''}
                </span>
                <button
                  onClick={() => setUploadedImages([])}
                  className="text-red-500 hover:text-red-600 text-sm"
                >
                  ‚úï
                </button>
              </div>
            )}
          </div>
        </div>
      </div>
    </div>
  );
};

export default AdminChatModal; 